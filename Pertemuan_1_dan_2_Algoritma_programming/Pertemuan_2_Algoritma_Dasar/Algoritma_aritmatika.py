x = 3
y = 2

# menjumlahkan
hasil = x + y
print(f"hasil penjumlahan dari {x} + {y} = {hasil}")

# pengurangan
hasil = x - y
print(f"hasil pengurangan dari {x} - {y} = {hasil}")

# Kali
hasil = x * y
print(f"hasil perkailan dari {x} * {y} = {hasil}")

# bagi
hasil = x / y
print(f"hasil pembagian dari {x} / {y} = {hasil}")
hasil = int(hasil)
print(f"hasil pembagian conversi dari {x} / {y} = {hasil}")
hasil = x // y
print(f"hasil pembagian singkat dari {x} // {y} = {hasil}")

# modulus
hasil = x % y
print(f"hasil modulus dari {x} % {y} = {hasil}")



